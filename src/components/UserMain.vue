<template>
  <GeminiScrollbar class="my-scroll-bar">
    <bread-crumb></bread-crumb>
    <div class="find_admin">
      <el-autocomplete
        popper-class="my-autocomplete"
        v-model="name"
        :fetch-suggestions="querySearchAsync"
        @select="handleSelectName"
        size="small"
        placeholder="用户名">
        <i
          class="el-icon-edit el-input__icon"
          slot="suffix">
        </i>
        <template slot-scope="{ item }">
          <span class="name">{{ item.name }}</span>
        </template>
      </el-autocomplete>
      <el-button type="primary" icon="el-icon-search" size="small" @click="getAdminInterface()">搜索</el-button>
    </div>
    <div class="admin_list">
      <template v-if="tableData.length>0">
        <el-table :data="tableData" stripe style="width: 100%" @selection-change="selectionChange" stripe
                  :row-key='tableData.id' fit highlight-current-row>
          <el-table-column type="selection" width="55" align="center"></el-table-column>
          <el-table-column prop="createTime" label="创建时间" :show-overflow-tooltip="showText">
            <template slot-scope="scope">
              <i class="el-icon-time"></i>
              <span style="margin-left: 10px">{{ scope.row.createTime }}</span>
            </template>
          </el-table-column>
          <el-table-column prop="name" label="用户名"></el-table-column>
          <el-table-column prop="sex" label="性别"></el-table-column>
          <el-table-column prop="age" label="年龄"></el-table-column>
          <el-table-column prop="phone" label="手机号" :show-overflow-tooltip="showText"></el-table-column>
          <el-table-column prop="email" label="邮箱" :show-overflow-tooltip="showText"></el-table-column>
          <el-table-column prop="userImg" label="头像">
            <template slot-scope="scope">
              <div class="user_img">
                <img :src="scope.row.userImg" alt="">
              </div>
            </template>
          </el-table-column>
          <el-table-column prop="signature" label="签名" :show-overflow-tooltip="showText"></el-table-column>
          <el-table-column fixed="right" label="操作" width="150">
            <template slot-scope="scope">
              <el-button type="text" size="small" @click="linkToDetail(scope.row.id)">查看</el-button>
              <el-button type="text" size="small" @click="linkToEdit(scope.row.id)">编辑</el-button>
              <el-button type="text" size="small">移除</el-button>
            </template>
          </el-table-column>
        </el-table>
      </template>
      <template v-else>
        <p>{{noData}}</p>
      </template>
    </div>
  </GeminiScrollbar>
</template>

<script>
  import BreadCrumb from './public/BreadCrumb';
  import moment from 'moment';  // use time plugs

  export default {
    name: "UserMain",
    data() {
      return {
        tableData: [],
        showText: true,
        name: '',
        timeout: null,
        timeout1: null,
        noData: ''
      }
    },
    components: {
      BreadCrumb
    },
    mounted() {
      this.getAdminInterface();
    },
    methods: {
      getAdminInterface() {
        let that = this;
        this.$http({
          method: 'get',
          url: '/allUser',
          params: {
            name: that.name
          }
        }).then(function (res) {
          that.getAdminData(res);
        }).catch(function (err) {
          console.log(err)
        });
      },
      getAdminData(data) {
        let resData = data.data.Datas;
        console.log(resData)
        if (resData.length > 0) {
          for (var i = 0; i < resData.length; i++) {
            if (!resData[i].signature || resData[i].signature == '') {
              resData[i].signature = '这个家伙懒的一笔！'
            }
            if (resData[i].age == '' || !resData[i].age) {
              resData[i].age = 'NULL';
            }
            if (resData[i].phone == '') {
              resData[i].phone = 'NULL';
            }
            if (resData[i].email == '') {
              resData[i].email = 'NULL';
            }
            if (resData[i].sex == '') {
              resData[i].sex = 'NULL';
            } else if (resData[i].sex == '1') {
              resData[i].sex = '男';
            } else {
              resData[i].sex = '女';
            }
            if (resData[i].createTime) {
              resData[i].createTime = moment(resData[i].createTime).format('YYYY-MM-DD HH:mm:ss');
            }
          }
          this.tableData = resData;
        } else {
          this.tableData = [];
          this.noData = '没有找到合适的资源';
        }
      },
      querySearchAsync(queryString, cb) {
        let restaurants = this.tableData;
        let results = queryString ? restaurants.filter(this.createStateFilter(queryString)) : restaurants;
        clearTimeout(this.timeout);
        this.timeout = setTimeout(() => {
          cb(results);
        }, 2000 * Math.random());
      },
      createStateFilter(queryString) {
        return (state) => {
          return (state.name.toLowerCase().indexOf(queryString.toLowerCase()) === 0);
        };
      },
      handleSelectName(item) {
        this.name = item.name;
      },
      linkToDetail(id) {
        this.linkTo(id, 'AdminDetail');
      },
      linkToEdit(id) {
        this.linkTo(id, 'EditAdmin');
      },
      linkTo(id, isAuth) {
        this.$http({
          method: 'get',
          url: '/adminDetail',
          params: {
            id: this.$store.state.userId
          }
        }).then((data) => {
          if (data.data.data.permissions == 'auth') {
            this.$router.push({name: isAuth, params: {id: id}})
          } else {
            if (id == this.$store.state.userId) {
              this.$router.push({name: isAuth, params: {id: id}})
            } else {
              this.$notify({
                title: '警告',
                message: '权限不足，无法编辑其他管理员信息！',
                type: 'warning'
              });
            }
          }
        }).catch((err) => {
          this.$message({message: '登录异常，请联系管理员！', type: 'error'});
          console.log(err)
        })
      },
      selectionChange(selection) {
        console.log(selection)
        /*this.groupOprate.ids = []
        for (let i = 0; i < selection.length; i++) {
          this.groupOprate.ids.push(selection[i].id)
        }
        console.log(this.groupOprate.ids)*/
      }
    }
  }
</script>

